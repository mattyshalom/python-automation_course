#to add to the end of a list

fruits = ["pineapple", "banana", "mango", "orange", "Apple"]
fruits.append("kiwi") #this add kiwi to our lists of fruits
print(fruits)


#to add an item as the first on the list
fruits = ["pineapple", "banana", "mango", "orange", "Apple"]
fruits.insert(0, "peach") #thos add peach as first on list
print(fruits)

#to delete item from list
fruits = ["pineapple", "banana", "mango", "orange", "Apple"]
fruits.remove("mango") # this remove mango from the list
print(fruits)

#to change an item in a list to another
fruits = ["pineapple", "banana", "mango", "orange", "Apple"]
fruits[2] = "strawberry" # this relace mango from the list to strawberry
print(fruits)
